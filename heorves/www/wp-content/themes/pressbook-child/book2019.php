<p>
Sait-on qu’une partie de la marine française a rallié, dès juin 1940, le général de Gaulle en Angleterre ? 
Qu’au lieu de livrer leurs navires aux Allemands, des marins civils et militaires ont désobéi au maréchal Pétain et à l’amiral Darlan pour rallier Londres ? 
Qu’ils ont, pendant plus de quatre ans, participé aux combats les plus glorieux contre les nazis ? 
Formées par l’amiral Muselier, les Forces navales françaises libres ont été le fer de lance d’opérations militaires légendaires. 
Pour la première fois, un livre relate leur épopée : les coups d’éclat contre les sous-marins allemands U-Boote, les victoires contre l’Afrika Korps de Rommel en Afrique, mais aussi les missions de ravitaillement de l’URSS par l’Arctique… 
Une histoire faite de victoires brillantes, de pertes amères et de courage, et dont les héros se nomment Honoré d’Estienne d’Orves, Philippe Kieffer ou encore les pêcheurs de l’île de Sein. 
Les marins libres sont enfin les seuls Français à débarquer en Normandie le 6 juin 1944, avant de participer, en août 1944, à la Libération de Paris puis à celle de l’Europe. 
Des anecdotes quotidiennes relatées par les soldats aux décisions stratégiques, en passant par des révélations sur les relations parfois conflictuelles entre de Gaulle et ses partenaires, Churchill notamment, toute la vie des « FNFL » nous est ici contée avec passion.</p>
